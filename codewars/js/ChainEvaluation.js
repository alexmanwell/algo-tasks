Number.prototype.add = function (a) {
  return this + a;
};

Number.prototype.subtract = function (a) {
  return this - a;
};

Number.prototype.multiply = function (a) {
  return this * a;
};

Number.prototype.divide = function (a) {
  return this / a;
};

Number.prototype.square = function () {
  return this ** 2;
};